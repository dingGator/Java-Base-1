public class CompoundAssignment {
    public static void main(String[] args) {
        int y = 0;
        y = y - 6;
        int x = 0;
        x = x + 2 * 5;
        System.out.println("y = [" + y + "]");
        System.out.println("x = [" + x + "]");
        int y1 = 0;
        y1 -= 6;
        int x1 = 0;
        x1 += 2 * 5;
        System.out.println("y1 = [" + y + "]");
        System.out.println("x1 = [" + x + "]");

    }
}
